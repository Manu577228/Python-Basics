# String methods are built-in functions that allow you to 
# perform operations like transforming, searching, and validating string data.

msg = "   Python is Fun   "

print("Stripped:", msg.strip())
print("Uppercase::", msg.upper())
print("Lowercase:", msg.lower())
print("Replace:", msg.replace("Fun", "Awesome"))
print("Index of 'is' :", msg.find("is"))
print("Split:", msg.strip().split())

