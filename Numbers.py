# Numbers in Python include int, float, and complex. Python handles big numbers and floating points natively.

x = 10
y = 3.5

print("Addition:", x + y)
print("Division:", x / y)
print("Floor Division:", x // y)
print("Remainder:", x % y)
print("Power:", x ** 2)

print("Type of x:", type(x))
print("Type of y:", type(y))

